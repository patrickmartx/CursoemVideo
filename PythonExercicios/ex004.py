# Faça um programa que leia algo pelo teclado  emostre na tela o seu tipo primitivo e todas as informações possíveis sobre ele

x = input("Digite algo: ")
print(x.isalnum())
print(x.isalpha())
print(x.isdecimal())
print(x.isnumeric())
print(x.isdecimal())
print(x.isascii())
print(x.isdigit())
print(x.isidentifier())
print(x.isprintable())